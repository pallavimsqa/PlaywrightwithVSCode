// String Properties & basic Access

let str = "Hello, World!";

//length - space is also included - index starts from 0
console.log(str.length);

// Access by Index
console.log(str[0]);
console.log(str[7]);
console.log(str.at(-10)); // ! - reverse counting
console.log(str.at(-6)); // w - reverse counting

// charAt() - returns character at the index
console.log(str.charAt(0));//H
 // charCodeAt() - returns the unicode value 
 console.log(str.charCodeAt(3));
 console.log(str.charCodeAt(0));

