//For Loop - help you to repeat the block of code

//Increment Operator - ++ increase the value by 1

//pre-increment  -> increment the value by 1 and then assign
let a=10;
let b = ++a;
console.log(b); // 11
console.log(a); // 11



// post-increment ->   assign the value first then increment the value by 1
let a=10;
let b = a++;
console.log(b); // 10
console.log(a); // 11 