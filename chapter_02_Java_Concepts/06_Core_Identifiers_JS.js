// Identifiers - name given to the container to store the value - a
// Literal -  the variable value stored - 20, 34
// Operator - sign used to connect the variable and the value  -  =
// Keyword -  the reserved words in JS  - var
// ; semicolon - its optional in JS

var a = 20;
a =34;
console.log(a);



// Rule of Identifier:
// 1. Must start with a letter, underscore, or dollar sign
// 2. Can contain letters, numbers, underscores, and dollar signs
// 3. Cannot start with a number
// 4. Cannot be a reserved keyword
// 5. Cannot contain spaces
// 6. Cannot contain special characters other than underscore and dollar sign
// 7. Case sensitive


var name = "Hello";
console.log(name);

var $name = "Hello";
console.log($name);

var _name = "Hello";
console.log(_name);

var name1 = "Hello";
console.log(name1);

var name_1 = "Hello";
console.log(name_1);

var name$1 = "Hello";
console.log(name$1);

var name_1$1 = "Hello";
console.log(name_1$1);

var name_1$1_2 = "Hello";
console.log(name_1$1_2);

var NAME = "pramod";
var name1 = "dutta";
console.log(NAME);
console.log(name1);

var π = 3.14;
console.log(π);


var नमस्ते = "Hi";
console.log(नमस्ते);

var _ = "Hello";
